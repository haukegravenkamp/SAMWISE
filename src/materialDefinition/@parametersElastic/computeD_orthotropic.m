function D = computeD_orthotropic(E11,E22,E33, v12,v13,v23, G23,G31,G12)


v21 = v12 .* E22 ./ E11;
v31 = v13 .* E33 ./ E11;
v32 = v23 .* E33 ./ E22;

 d = (1-v12*v21-v23*v32-v13*v31-2*v12*v23*v31);

D = [ (1-v23*v32)/d*E11, (v21+v31*v23)/d*E11,  (v31+v21*v32)/d*E11,   0,   0,   0;...
    (v21+v31*v23)/d*E11,   (1-v13*v31)/d*E22,  (v32+v31*v12)/d*E22,   0,   0,   0;...
    (v31+v21*v32)/d*E11, (v32+v31*v12)/d*E22,    (1-v12*v21)/d*E33,   0,   0,   0;...
    0,                   0,                    0, G23,   0,   0;...
    0,                   0,                    0,   0, G31,   0;...
    0,                   0,                    0,   0,   0, G12];






% C = zeros(6,6);
% C(1,1:3) = [    1/E1, -nu12/E1, -nu13/E1];
% C(2,1:3) = [-nu12/E1,     1/E2, -nu23/E2];
% C(3,1:3) = [-nu13/E1, -nu23/E2,    -1/E3];
% C(4,4)   = G23;
% C(5,5)   = G31;
% C(6,6)   = G12;
% 
% D = inv(C);

%   2012-2024 Hauke Gravenkamp, gravenkamp.research@gmail.com
 
end
